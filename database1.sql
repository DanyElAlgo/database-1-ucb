CREATE DATABASE database_ucb

USE database_ucb


CREATE TABLE PLAYER(
ID			 INT		 IDENTITY(1, 1) NOT NULL PRIMARY KEY,
USERNAME	 VARCHAR(20)				NOT NULL,
PLAY_TIME	 DATETIME,
USER_LEVEL	 INT
)

