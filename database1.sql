CREATE TABLE ITEM(
ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
ITEM_DESCRIPTION VARCHAR(100) NOT NULL,
)

CREATE TABLE INVENTORY(
ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
UNLOCK_STATUS BINARY,
ID_PLAYER INT FOREIGN KEY REFERENCES PLAYER(ID),
ID_ITEM INT FOREIGN KEY REFERENCES ITEM(ID)
)

CREATE TABLE GAME_SERVER(
ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
CONECTION INT,
ID_PLAYER INT FOREIGN KEY REFERENCES PLAYER(ID)
)

CREATE TABLE PLAYER_SITUATION(
ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
POINTS INT,
HEALTH INT,
ID_INVENTORY INT FOREIGN KEY REFERENCES INVENTORY(ID)
)

CREATE TABLE ONLINE_MATCH(
ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
ID_PLAYER INT FOREIGN KEY REFERENCES PLAYER(ID),
ID_SITUATION INT FOREIGN KEY REFERENCES PLAYER_SITUATION(ID)
)


